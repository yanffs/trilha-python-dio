# a lista é versátil pode admitir qualquer tipo de dado

# duas maneiras de construir lista utilizando colchetes e a função list
# 1) opcao de declarar uma lista
frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = []
print(frutas)

# 2) opcao de declarar uma lista
letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
